def speak_to_grandma (phrase)
  if !(phrase == phrase.upcase)
    puts "HUH?! SPEAK UP, SONNY!"
  elsif phrase == phrase.upcase && phrase != "I LOVE YOU GRANDMA!"
    puts "NO, NOT SINCE 1938!"
  else "I LOVE YOU TOO PUMPKIN!"
end 

